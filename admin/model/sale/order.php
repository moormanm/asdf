	}

	public function getOrder($order_id) {
           return ShopciergeOrder::getOrder($order_id, $this->db);
	}

	public function deleteOrder($order_id) {
           $sql = "DELETE FROM " . DB_PREFIX . "order WHERE order_id = ' " . $order_id . "'";
