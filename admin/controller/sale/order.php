        private function isValidOrderStatusID($id) {
	     $this->load->model('localisation/order_status');
	     $statuses = $this->model_localisation_order_status->getOrderStatuses();
             foreach( $statuses as $status ) {
                if( $status['order_status_id'] == $id ) {
                   return true;
                }
             }
             return false;
        }

         
        public function changeStatus() {
               
           if ($this->request->server['REQUEST_METHOD'] == 'POST')  {
                $this->load->model('sale/order');
                
                
                if($this->isValidOrderStatusID($_POST['newStatus']))  {
                   $order_info = $this->model_sale_order->updateOrderStatus(intval($_POST['order_id']), intval($_POST['newStatus']) );
                }
                $this->request->get['order_id'] = $_POST['order_id'];
                $this->info();
        }
 

